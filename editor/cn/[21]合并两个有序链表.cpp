/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* mergeTwoLists(ListNode* l1, ListNode* l2) {
        if (l1 == nullptr && l2 == nullptr)
            return nullptr;

        // 哨兵节点
        ListNode dummpy = ListNode(-1);
        // 当前节点
        ListNode *cur = &dummpy;

        // 都不为空
        while (l1 && l2) {

            // 依次使用该节点
            if (l1->val < l2->val) {
                cur->next = l1;
                cur = l1;
                l1 = l1->next;
            } else {
                cur->next = l2;
                cur = l2;
                l2 = l2->next;
            }
        }

        // 链接剩下的节点
        if (l1) {
            cur->next = l1;
        } else {
            cur->next = l2;
        }

        return dummpy.next;
    }
};
