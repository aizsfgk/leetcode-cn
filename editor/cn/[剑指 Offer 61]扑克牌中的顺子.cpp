class Solution {
public:
    bool isStraight(vector<int>& nums) {
        sort(nums.begin(), nums.end());

        unordered_map<int, int> search;
        for (int num : nums) {
            search[num]++;
        }

        int base = nums[4];

        int loop = 4;
        while (loop > 0) {
            if (search[base-1] > 0) {
                search[base-1]--;
            } else {
                if (search[0] > 0) {
                    search[0]--;
                } else {
                    return false;
                }
            }
            base--;
            loop--;
        }
        return true;
    }
};
