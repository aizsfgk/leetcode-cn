/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        vector<vector<int>> ans;
        if (!root)
            return ans;
        queue<TreeNode *> que;
        que.push(root);
        while (!que.empty()) {
            int sz = que.size();
            ans.push_back(vector<int>());
            for (int i=0; i<sz; i++) {
                auto node = que.front(); que.pop();
                ans.back().push_back(node->val);

                if (node->left) {
                    que.push(node->left);
                }

                if (node->right) {
                    que.push(node->right);
                }
            }
        }

        return ans;
    }
};
