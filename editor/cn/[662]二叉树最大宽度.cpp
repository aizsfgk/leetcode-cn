/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
private:
    long long maxWidth = 0;
public:
    int widthOfBinaryTree(TreeNode* root) {
        if (root == nullptr)
            return 0;

        queue<pair<TreeNode*, long long>> que;
        que.push({root, 1}); // ä¸€ä¸ªèŠ‚ç‚¹ï¼Œå®½åº¦å½“ç„¶æ˜¯1

        while (!que.empty()) {
            int size = que.size();

            long long left = que.front().second, right = que.front().second;

            for (int i=size-1; i>=0; i--) {
                auto ele = que.front(); que.pop();

                right = ele.second;

                if (ele.first->left) {
                    que.push({ele.first->left, 2 * (right - left) });
                }

                if (ele.first->right) {
                    que.push({ele.first->right, 2 * (right-left)+1 });
                }

                maxWidth = max(maxWidth, right-left+1);
            }
        }

        return maxWidth;
    }
};
