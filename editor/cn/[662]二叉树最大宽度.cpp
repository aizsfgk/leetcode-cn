/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
private:
    struct SData {
        long long val;
        TreeNode *root;
        SData() : val(0), root(nullptr) {}
        SData(long long x) : val(x), root(nullptr) {}
        SData(long long x, TreeNode *r) : val(x), root(r) {}
    };
public:
    long long ans;

    int widthOfBinaryTree(TreeNode* root) {
        if (root == nullptr) {
            return 0;
        }

        queue<SData*> q;
        q.push(new SData(0, root));

        long long v = 0;
        long long width = 0;
        while (!q.empty()) {
            int size = q.size();
            width = q.back()->val - q.front()->val + 1;

            for (int i=0; i< size; i++) {
                SData *node = q.front(); q.pop();

                if (node->root->left) {
                    v = node->val * 2 + 1;
                    q.push(new SData(v, node->root->left));
                }

                if (node->root->right) {
                    v = node->val * 2 + 2;
                    q.push(new SData(v, node->root->right));
                }
            }

            ans = max(ans, width);

        }

        return (int)ans;
    }
};
