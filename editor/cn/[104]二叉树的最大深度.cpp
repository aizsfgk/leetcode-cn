/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
private:
    int max(int a, int b) {
        if (a >= b) {
            return a;
        } else {
            return b;
        }
    }
public:
    int maxDepth(TreeNode* root) {
        if (root == nullptr) { /// éœ€è¦ä¿ç•™çš„
            return 0;
        }  else {
            return 1 + max(maxDepth(root->left), maxDepth(root->right)); /// éœ€è¦ä¿ç•™çš„
        }
    }
};
