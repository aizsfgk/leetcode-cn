class Solution {
public:
    string addStrings(string num1, string num2) {
        // ä¸¤å­—ç¬¦ä¸²ç›¸åŠ 

        int n = num1.size() - 1;
        int m = num2.size() - 1;
        int carry = 0;
        string ans = "";
        while (n>=0 || m >= 0 || carry) {
            int one = n >= 0 ? num1[n] - '0' : 0;
            int two = m >= 0 ? num2[m] -  '0' : 0;

            int sum = one + two + carry;
            carry = sum / 10;

            char tmp = (sum % 10) + '0';
            ans.insert(0, 1, tmp);
            n--;
            m--;
        }

        return ans;

    }
};
