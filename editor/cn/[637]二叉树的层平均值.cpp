/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
private:
    vector<double> ret;
public:
    vector<double> averageOfLevels(TreeNode* root) {
        // äºŒå‰æ ‘çš„å±‚åºéå†
        if (root == nullptr) {
            return ret;
        }

        queue<TreeNode *> q;

        q.push(root);


        int size = 0;
        double sum = 0.0;
        double one = 0.0;

        while (!q.empty()) {
            size = q.size();
            sum = 0.0;

            for (int i=0; i<size; i++) {
                auto ele = q.front(); q.pop();
                sum += ele->val;

                if (ele->left) {
                    q.push(ele->left);
                }

                if (ele->right) {
                    q.push(ele->right);
                }
            }

            one = sum / size;
            ret.push_back(one);
        }

        return ret;
    }
};
